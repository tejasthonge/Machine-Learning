#4. Reinforcement Machine Learning
'''
Reinforcement learning is a type of machine learning where an agent learns to interact with an environment by performing 
actions and receiving rewards or penalties based on its actions. The goal of reinforcement learning is to learn a policy,
 which is a mapping from states to actions, that maximizes the expected cumulative reward over time.

There are two main types of reinforcement learning:

    *1Model-based reinforcement learning: 
        -In model-based reinforcement learning, the agent learns a model of the environment, 
        including the transition probabilities between states and the rewards associated with each state-action pair.
          The agent then uses this model to plan its actions in order to maximize its expected reward. 
          Some popular model-based reinforcement learning algorithms include Value Iteration and Policy Iteration.
    
    *Model-free reinforcement learning: 
        -In model-free reinforcement learning, the agent learns a policy directly from experience without explicitly 
        building a model of the environment. The agent interacts with the environment and updates its policy based on 
        the rewards it receives. Some popular model-free reinforcement learning algorithms include Q-Learning, SARSA, 
        and Deep Reinforcement Learning.

'''